package arrays;

public class Assign15 {
	public static void main(String []args) {


		//with no parameter
        () -> system.out.println("whith no parameters ");

		//with one parameter
		(x) -> system.out.println("with one parameters: " + x );

		//with two parameter
		(x,y) -> system.out.println("with two parameters: " + x +" , " +y);

		//with three parameters
		(x,y,z) -> system.out.println("with three parameters: " + x + " , "+ y +" , "+ z);
		}



}


